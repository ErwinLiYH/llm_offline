"""Maze map layouts for all CrossMaze variants (plain 0/1 grids only)."""


def maze_from_strings(rows: list[str]) -> list[list[int]]:
    if not rows or not rows[0]:
        raise ValueError("maze rows must be non-empty")
    width = len(rows[0])
    if any(len(row) != width for row in rows):
        raise ValueError(f"maze rows must have equal width: {rows}")
    maze = []
    for row in rows:
        parsed_row = []
        for cell in row:
            if cell == "#":
                parsed_row.append(1)
            elif cell == ".":
                parsed_row.append(0)
            else:
                raise ValueError(f"Unsupported maze cell: {cell!r}")
        maze.append(parsed_row)
    return maze


